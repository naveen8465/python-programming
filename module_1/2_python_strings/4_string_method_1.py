#len(), upper(), lower(), title(),count()


l="kalyani bhargav naveen"

#len() is used to find the length 
print(len(l))

#.upper is used to convert into upper case
print(l.upper())

#.lower is used to covert into lower case
print(l.lower())

#.title is used to convert 11st letter into upper case
print(l.title())


print(l.count("n"))

print(l.title().count('n'))
